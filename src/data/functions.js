export function isDateCorrect(date) {
    return date.split('.').length === 3 && date.match('[0-9]{2}\.[0-9]{2}\.[0-9]{4}') && moment(date, 'DD.MM.YYYY').isValid()
}

export const isEmailCorrect = (email) => {
    return String(email)
        .toLowerCase()
        .match(
            /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|.(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
        );
};

export const getTimeWithTz = function (time, format = "DD.MM.YYYY HH:mm") {
    require('moment-timezone'); // important, if you remove this, it will crash
    var mtz = moment.tz;
    let m = mtz(time, "UTC").tz(mtz.guess())
    return m.format(format)
}

